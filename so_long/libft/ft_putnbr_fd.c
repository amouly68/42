#include<unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	int	nb1;
	int	b;

	b = 0;
	if (fd < 0 || fd > 255)
		return ;
	if (n == -2147483648)
		n = ++b + n;
	if (n < 0)
	{
		n = -n;
		write(fd, "-", 1);
	}
	nb1 = n % 10 + '0';
	n = n / 10;
	if (n != 0)
		ft_putnbr_fd(n, fd);
	if (b == 1)
		nb1++;
	write (fd, &nb1, 1);
}
