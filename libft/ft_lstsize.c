#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int			a;
	t_list		*ptr;

	a = 0;
	ptr = lst->next;
	while (ptr != NULL)
	{
		ptr = lst->next;
		a++;
	}
	return (a);
}
