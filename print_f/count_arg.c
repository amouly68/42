int count_arg(const char *str)
{
	int i;
	int count;

	count = 0;
	i = 0;
	while (str[i])
	{
		if 	((str[i] == '%') && (str[i+1] == 'c'
				|| str[i+1] == 's' || str[i+1] == 'p' 
				|| str[i+1] == 'd' || str[i+1] == 'i' 
				|| str[i+1] == 'u' || str[i+1] == 'x' 
				|| str[i+1] == 'X'))
		{
			count ++;
			i++;
		}
		if 	((str[i] == '%') && (str[i+1] == '%'))
			i++;
		i++;
	}
	return (count);
}
